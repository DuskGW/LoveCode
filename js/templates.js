export function openTemplateModal(callback) {
  const modal = document.createElement('div');
  modal.className = 'template-modal';
  modal.innerHTML = `
    <div class="template-content">
      <div class="template-header">
        <h3>根据场景选择祝福语模板</h3>
        <button class="template-close" id="template-close">&times;</button>
      </div>
      <div class="template-categories" id="template-categories">
      </div>
      <div class="template-list" id="template-list">
      </div>
    </div>
  `;

  document.body.appendChild(modal);

  const categoriesContainer = document.getElementById('template-categories');
  const listContainer = document.getElementById('template-list');

  Object.keys(TEMPLATES).forEach(key => {
    const catBtn = document.createElement('button');
    catBtn.className = 'template-category';
    catBtn.textContent = TEMPLATES[key].name;
    catBtn.dataset.category = key;
    catBtn.addEventListener('click', () => {
      document.querySelectorAll('.template-category').forEach(b => b.classList.remove('active'));
      catBtn.classList.add('active');
      renderTemplateItems(key);
    });
    categoriesContainer.appendChild(catBtn);
  });

  function renderTemplateItems(category) {
    listContainer.innerHTML = '';
    TEMPLATES[category].items.forEach(item => {
      const itemEl = document.createElement('div');
      itemEl.className = 'template-item';
      itemEl.innerHTML = `
        <h4>${item.name}</h4>
        <p>${item.content.substring(0, 100)}...</p>
      `;
      itemEl.addEventListener('click', () => {
        callback(item.content);
        modal.remove();
      });
      listContainer.appendChild(itemEl);
    });
  }

  document.querySelector('.template-category').click();

  document.getElementById('template-close').addEventListener('click', () => {
    modal.remove();
  });

  modal.addEventListener('click', (e) => {
    if (e.target === modal) {
      modal.remove();
    }
  });
}